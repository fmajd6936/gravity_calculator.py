# Калькулятор гравитации
# Рассчитывает силу гравитации между двумя объектами в космосе

G = 6.674 * (10 ** -11)  # Гравитационная постоянная

# Запрос масс объектов и расстояния между ними
m1 = float(input("Введите массу первого объекта (в кг): "))
m2 = float(input("Введите массу второго объекта (в кг): "))
r = float(input("Введите расстояние между объектами (в метрах): "))

# Рассчитываем силу гравитации
F = G * m1 * m2 / r ** 2

# Выводим результат
print("Сила гравитации между объектами: ", F, "Н")
